import { getRandomDate } from '@/utils/date/getRandomDate';
import { useMemo, useRef, useState } from 'react';

export function HeartAndDislikeFooter() {
  const randomDate = useMemo(() => getRandomDate(), []);
  const [isHeartClicked, setIsHeartClicked] = useState(false);
  const [isDislikeClicked, setIsDislikeClicked] = useState(false);
  const userHasClicked = useRef(false);

  function updateClicked({
    isHeartClicked,
    isDislikeClicked
  }: {
    isHeartClicked: boolean;
    isDislikeClicked: boolean;
  }) {
    return () => {
      if (!userHasClicked.current) userHasClicked.current = true;
      setIsHeartClicked(isHeartClicked);
      setIsDislikeClicked(isDislikeClicked);
    };
  }

  return (
    <footer className='am-footer-of-comments'>
      <p className='date-of-comment paraghaph'>{randomDate}</p>
      <aside className='footer-right'>
        <aside
          className='heart-of-comment'
          onClick={updateClicked({
            isDislikeClicked: false,
            isHeartClicked: true
          })}
        >
          {isHeartClicked && userHasClicked.current ? (
            <HeartCommentFilled className='am-svg am-heart ' />
          ) : (
            <HeartComment className='am-svg am-heart ' />
          )}
        </aside>
        <aside
          className='dislike-of-comment'
          onClick={updateClicked({
            isDislikeClicked: true,
            isHeartClicked: false
          })}
        >
          {isDislikeClicked && userHasClicked.current ? (
            <DisLikeCommentFilled className='am-svg am-dislike ' />
          ) : (
            <DisLikeComment className='am-svg am-dislike ' />
          )}
        </aside>
      </aside>
    </footer>
  );
}

